window.populateAppleBackgroundSelect = function(select, options = {}) {
  const extras = options.extras || [];
  const includeNone = options.includeNone || false;
  const noneLabel = options.noneLabel || "No Wallpaper";
  const defaultValue = options.defaultValue || "";
  const groups = new Map();
  const items = [...extras, ...window.APPLE_BACKGROUNDS];
  select.innerHTML = "";
  if (includeNone) {
    const option = document.createElement("option");
    option.value = "none";
    option.textContent = noneLabel;
    select.appendChild(option);
  }
  for (const item of items) {
    const group = item.group || "Apple";
    if (!groups.has(group)) groups.set(group, []);
    groups.get(group).push(item);
  }
  for (const [groupName, groupItems] of groups.entries()) {
    const optgroup = document.createElement("optgroup");
    optgroup.label = groupName;
    for (const item of groupItems) {
      const option = document.createElement("option");
      option.value = item.id;
      option.textContent = item.label;
      optgroup.appendChild(option);
    }
    select.appendChild(optgroup);
  }
  if (defaultValue) select.value = defaultValue;
};

window.resolveAppleBackground = function(value, extras = []) {
  if (!value || value === "none") return "";
  const all = [...extras, ...window.APPLE_BACKGROUNDS];
  const match = all.find((item) => item.id === value);
  return match ? match.url : "";
};
